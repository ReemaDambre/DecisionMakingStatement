public class If_Else_If_Lader {

	public static void main(String[] args) {
		String city = "Agra";
		if(city =="Meerut") {
			System.out.println("city is meerut");
		}else if (city =="Noida") {
			System.out.println("city is Noida");
			
		}else if(city =="Agra") {
			System.out.println("city is Agra");
			
		}else {
			System.out.println(city);
		}

	}

}
